package fundamentos.operadores

fun main(args: Array<String>) {
    var a: Int =7
    var b: Int=3
//    operação aditiva
    b +=a //b=b+a
    println(b)
    //    operação subitrativa
    b -= a //b=b-a
    println(b)
    b*=2 //b=b*2
    println(b)
    b/=2 //b=b/2
    println(b)
    b%=2
    println(b)
}